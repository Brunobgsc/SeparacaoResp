//A classe Estoque manipula a quantidade de produtos cadastrados 

package br.com.separacao.estoque;


public class EstoqueClass {

	
	private int id;
	private String qtd;
	
			
	public EstoqueClass() {
		super();
	}
		
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getQtd() {
		return qtd;
	}
	public void setQtd(String qtd) {
		this.qtd = qtd;
	}


	}


